package de.colorscheme.output;

/**
 * The enum contains the different color harmonies that can be used to calculate from the main colors.
 *
 * @author &copy; 2024 Elisa Johanna Woelk | elisa-johanna.woelk@outlook.de | @fenris_22127
 * @version 1.1
 * @since 17.0.1
 */
public enum ColorHarmony {
    /**
     * The complementary color harmony
     */
    COMPLEMENTARY,
    /**
     * The split-complementary color harmony
     */
    SPLITCOMPLEMENTARY,
    /**
     * The analogous color harmony
     */
    ANALOGOUS,
    /**
     * The triadic color harmony
     */
    TRIADIC,
    /**
     * The tetradic color harmony
     */
    TETRADIC,
    /**
     * The monochromatic color harmony
     */
    MONOCHROMATIC
}
